const goFileHeader = "// Code generated by github.com/sergei-svistunov/go-ssr. DO NOT EDIT.\n"

type Generator struct {
	dir            string
	prod           bool
	webDir         string
	webPkgName     string
	depsPackage    string
	depsType       string
	routes         map[string]*route.Route
	assets         *Assets
	projectCmd     *exec.Cmd
	projectCmdDone chan struct{}
	projectCmdEnv  map[string]string
	goRunArgs      string
}

func New(cfg *config.Config) *Generator {
	return &Generator{
		dir:           cfg.Dir,
		prod:          cfg.Prod,
		webDir:        cfg.WebDir,
		webPkgName:    cfg.WebPackage,
		depsPackage:   cfg.DepsPackage,
		depsType:      cfg.DepsType,
		routes:        make(map[string]*route.Route),
		projectCmdEnv: cfg.Env,
		goRunArgs:     cfg.GoRunArgs,
	}
}

func (g *Generator) Analyze() error {
	g.routes = make(map[string]*route.Route)

	assets, err := AssetsFromDir(g.webDir)
	if err != nil {
		return err
	}
	g.assets = assets

	pagesDir := filepath.Join(g.webDir, "pages")
	if err := filepath.WalkDir(pagesDir, func(p string, d fs.DirEntry, err error) error {
		// staticEmbedDirName is a reserved top-level directory under pages/ used
		// as the staging dir for the embedded static-file blob. A real route
		// folder cannot share this name.
		if d.IsDir() && d.Name() == staticEmbedDirName && filepath.Dir(p) == pagesDir {
			return filepath.SkipDir
		}
		if !d.IsDir() || isDirEmpty(p) {
			return nil
		}

		routePath, err := filepath.Rel(pagesDir, p)
		if err != nil {
			return fmt.Errorf("could not determine relative path for route: %w", err)
		}

		if runtime.GOOS == "windows" {
			routePath = strings.Replace(routePath, "\\", "/", -1)
		}

		r, err := route.FromDir(p, func(file string) string {
			result := g.assets.GetImageAsset(path.Join(routePath, file))
			if result == "" {
				return file
			}
			return result
		})
		if err != nil {
			return err
		}

		g.routes[path.Join("/", routePath)] = r

		return nil
	}); err != nil {
		return err
	}

	return nil
}

func (g *Generator) Generate() error {
	hasStatic, staticURLs, err := g.genStaticFiles()
	if err != nil {
		return fmt.Errorf("could not generate static files: %w", err)
	}

	if hasStatic {
		if collisions := g.collisionsWithRoutes(staticURLs); len(collisions) > 0 {
			return fmt.Errorf(
				"static asset URL(s) collide with route path(s): %s — these routes would be silently shadowed by the embedded static handler. Rename the route(s) or change the webpack outputPath to disambiguate",
				strings.Join(collisions, ", "),
			)
		}
	}

	if err := g.genHandler(hasStatic); err != nil {
		return fmt.Errorf("could not generate handler: %w", err)
	}

	for rPath, r := range g.routes {
		if err := g.genRoute(rPath, r); err != nil {
			return fmt.Errorf("could not generate route %s: %w", rPath, err)
		}

		if err := g.genRouteDP(rPath, r); err != nil {
			return fmt.Errorf("could not generate route dataprovider %s: %w", rPath, err)
		}
	}

	return nil
}

func (g *Generator) getRoutesPaths() []string {
	paths := make([]string, 0, len(g.routes))
	for p := range g.routes {
		paths = append(paths, p)
	}
	sort.Strings(paths)

	return paths
}

func (g *Generator) depsPackageName() string {
	return path.Base(g.depsPackage)
}

// collisionsWithRoutes returns any static URL keys that the SSR mux would
// route to a registered route — accounting for dynamic-param segments
// (folders named `_paramName_`). The generated handler tries the static
// handler first, so a colliding route would be silently shadowed.
func (g *Generator) collisionsWithRoutes(staticURLs []string) []string {
	if len(staticURLs) == 0 || len(g.routes) == 0 {
		return nil
	}
	routeSegs := make([][]string, 0, len(g.routes))
	for rp := range g.routes {
		routeSegs = append(routeSegs, splitRoutePath(rp))
	}
	var collisions []string
	for _, u := range staticURLs {
		us := splitRoutePath(u)
		for _, rs := range routeSegs {
			if routeMatchesSegments(rs, us) {
				collisions = append(collisions, u)
				break
			}
		}
	}
	sort.Strings(collisions)
	return collisions
}

func splitRoutePath(p string) []string {
	p = strings.TrimSuffix(p, "/")
	if p == "" {
		return nil
	}
	return strings.Split(strings.TrimPrefix(p, "/"), "/")
}

func routeMatchesSegments(routeSegs, urlSegs []string) bool {
	if len(routeSegs) != len(urlSegs) {
		return false
	}
	for i, rs := range routeSegs {
		if isParamSegment(rs) {
			continue
		}
		if rs != urlSegs[i] {
			return false
		}
	}
	return true
}

func isParamSegment(s string) bool {
	return len(s) >= 3 && s[0] == '_' && s[len(s)-1] == '_' && !strings.Contains(s[1:len(s)-1], "_")
}

func (g *Generator) genHandler(hasStatic bool) error {
	buf := gobuf.New()

	buf.WriteStringLn(goFileHeader)
	buf.WriteStringLn("package pages")

	buf.WriteStringLn("import (")
	buf.WriteQuotedString("net/http", "\n\n")
	buf.WriteQuotedString("github.com/sergei-svistunov/go-ssr/pkg/mux", "\n")
	if g.depsPackage != "" {
		buf.WriteQuotedString(g.depsPackage, "\n")
	}
	buf.WriteString("\n")
	for _, rPath := range g.getRoutesPaths() {
		if rPath == "/" {
			continue
		}
		buf.WriteString(getRoutePackageAlias(rPath))
		buf.WriteQuotedString(path.Join(g.webPkgName, "pages", rPath), "\n")
	}

	buf.WriteStringLn(")")

	// func NewSsrHandler
	if g.depsPackage != "" {
		buf.WriteStringLn("func NewSsrHandler(d *" + g.depsPackageName() + "." + g.depsType + ", opts mux.Options) http.Handler {")
	} else {
		buf.WriteStringLn("func NewSsrHandler(opts mux.Options) http.Handler {")
	}
	if hasStatic {
		buf.WriteStringLn("ssrH := mux.New(map[string]mux.Route{")
	} else {
		buf.WriteStringLn("return mux.New(map[string]mux.Route{")
	}
	for _, rPath := range g.getRoutesPaths() {
		buf.WriteQuotedString(rPath)
		buf.WriteString(": ")
		pkgPrefix := ""
		if rPath != "/" {
			pkgPrefix = getRoutePackageAlias(rPath) + "."
		}
		if g.depsPackage != "" {
			buf.WriteStringLn(pkgPrefix + "NewRoute(" + pkgPrefix + "NewDP(d)),")
		} else {
			buf.WriteStringLn(pkgPrefix + "NewRoute(" + pkgPrefix + "NewDP()),")
		}
	}

	buf.WriteStringLn("}, opts)")
	if hasStatic {
		buf.WriteStringLn("return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {")
		buf.WriteStringLn("if ssrServeStatic(w, r) { return }")
		buf.WriteStringLn("ssrH.ServeHTTP(w, r)")
		buf.WriteStringLn("})")
	}
	buf.WriteStringLn("}")

	formattedCode, err := buf.Formatted()
	if err != nil {
		return fmt.Errorf("could not format generated code: %w\n%s", err, buf.String())
	}

	if err := os.WriteFile(filepath.Join(g.webDir, "pages", "ssrhandler_gen.go"), formattedCode, 0755); err != nil {
		return fmt.Errorf("could not write handler.go: %w", err)
	}

	return nil
}

func (g *Generator) genRoute(rPath string, r *route.Route) error {
	buf := gobuf.New()

	buf.WriteStringLn(goFileHeader)
	buf.WriteString("package ")
	buf.WriteStringLn(path.Base(path.Join("pages", rPath)))

	buf.WriteStringLn("import (")
	buf.WriteQuotedString("context", "\n")
	buf.WriteQuotedString("io", "\n")
	if len(r.Template().GetForms()) > 0 {
		buf.WriteQuotedString("net/http", "\n")
	}
	buf.WriteString("\n")
	if len(r.Template().GetForms()) > 0 {
		buf.WriteQuotedString("github.com/sergei-svistunov/go-ssr/pkg/form", "\n")
	}
	buf.WriteQuotedString("github.com/sergei-svistunov/go-ssr/pkg/mux", "\n")
	buf.WriteStringLn(")")

	routeVariables := r.Template().GetVariables()

	// type RouteData
	buf.WriteStringLn("type RouteData struct {")
	for _, variable := range routeVariables {
		buf.WriteString(getExportedName(variable.Name))
		buf.WriteString(" ")
		buf.WriteStringLn(variable.Type)
	}
	buf.WriteStringLn("}")
	buf.WriteString("\n")

	// Forms types
	for _, form := range r.Template().GetForms() {
		formValuesName := getFormValuesTypeName(form.Name)
		form.Node.DPCtxFieldName = getFormCtxField(form.Name)

		buf.WriteStringLn("type " + formValuesName + " struct {")
		buf.WriteStringLn("	form.BaseFormValues")
		for _, element := range form.Elements {
			fieldName := getExportedName(element.Name)
			buf.WriteString(fieldName)
			switch element.Type {
			case template.FormElementInput:
				if element.IsMultiple {
					buf.WriteStringLn(" form.InputMultiple[" + element.GoType + "]")
				} else {
					buf.WriteStringLn(" form.Input[" + element.GoType + "]")
				}
				for _, n := range element.Nodes {
					if inp, ok := n.(*node.SsrInput); ok {
						inp.FormFieldName = fieldName
					}
				}
			case template.FormElementInputFile:
				if element.IsMultiple {
					buf.WriteStringLn(" form.FileMultiple")
				} else {
					buf.WriteStringLn(" form.File")
				}
				if inp, ok := element.Nodes[0].(*node.SsrInput); ok {
					inp.FormFieldName = fieldName
				}
			case template.FormElementTextarea:
				buf.WriteStringLn(" form.Textarea")
				if ta, ok := element.Nodes[0].(*node.SsrTextarea); ok {
					ta.FormFieldName = fieldName
				}
			case template.FormElementSelect:
				if element.IsMultiple {
					buf.WriteStringLn(" form.SelectMultiple[" + element.GoType + "]")
				} else {
					buf.WriteStringLn(" form.Select[" + element.GoType + "]")
				}
				if sel, ok := element.Nodes[0].(*node.SsrSelect); ok {
					sel.FormFieldName = fieldName
				}
			default:
				return fmt.Errorf("invalid form element type %d for element %q", element.Type, element.Name)
			}
		}
		buf.WriteStringLn("}")
		buf.WriteString("\n")
	}

	// RouteDataProvider
	buf.WriteStringLn("type RouteDataProvider interface{")
	buf.WriteStringLn("Data(ctx context.Context, r *mux.Request, w mux.ResponseWriter, data *RouteData) error")

	if r.Template().GetContentNode() != nil && r.Template().GetContentNode().Default == "" {
		buf.WriteStringLn("DefaultRoute(ctx context.Context, r *mux.Request) (string, error)")
	}

	for _, form := range r.Template().GetForms() {
		buf.WriteStringLn(getFormInitMethod(form.Name) + "(ctx context.Context, r *mux.Request, w mux.ResponseWriter, data *" + getFormValuesTypeName(form.Name) + ") error")
		buf.WriteStringLn(getFormProcessMethod(form.Name) + "(ctx context.Context, r *mux.Request, w mux.ResponseWriter, data *" + getFormValuesTypeName(form.Name) + ") error")
	}
	buf.WriteStringLn("}\n")

	// type ssrRoute
	buf.WriteStringLn("type ssrRoute struct { dp RouteDataProvider }")
	buf.WriteStringLn("func NewRoute(dp RouteDataProvider) mux.Route { return &ssrRoute{dp: dp} }")

	// func (rt *ssrRoute) GetDataContext
	buf.WriteStringLn("func (rt *ssrRoute) GetDataContext(ctx context.Context, r *mux.Request, w mux.ResponseWriter, child mux.DataContext) (mux.DataContext, error) {")

	buf.WriteStringLn("dataCtx := &dataContext{")
	buf.WriteStringLn("	RouteDataContext: mux.RouteDataContext{")
	buf.WriteStringLn("		Child:  child,")
	buf.WriteStringLn("		Assets: []string{")
	for _, asset := range g.assets.GetTags(rPath) {
		buf.WriteQuotedString(asset, ",\n")
	}
	buf.WriteStringLn("		},")
	buf.WriteStringLn("	},")
	buf.WriteStringLn("}")
	buf.WriteString("\n")

	for _, form := range r.Template().GetForms() {
		dCtxFormField := "dataCtx." + getFormCtxField(form.Name)
		buf.WriteStringLn(dCtxFormField + ".BaseFormValues.SetElements([]form.Element{")
		for _, element := range form.Elements {
			buf.WriteStringLn("&" + dCtxFormField + "." + getExportedName(element.Name) + ",")
		}
		buf.WriteStringLn("})")
		buf.WriteString("\n")

		buf.WriteStringLn("if err := rt.dp." + getFormInitMethod(form.Name) + "(ctx, r, w, &" + dCtxFormField + "); err != nil {")
		buf.WriteStringLn("	return nil, err")
		buf.WriteStringLn("}")
		buf.WriteString("\n")
	}

	if len(r.Template().GetForms()) > 0 {
		buf.WriteStringLn("if r.Method == http.MethodPost {")
		buf.WriteStringLn("formId, err := form.Parse(r)")
		buf.WriteStringLn("if err != nil {")
		buf.WriteStringLn("	return nil, err")
		buf.WriteStringLn("}")
		buf.WriteStringLn(`switch formId {`)
		for _, form := range r.Template().GetForms() {
			formField := getFormCtxField(form.Name)
			buf.WriteString("case ")
			buf.WriteQuotedString(form.Name)
			buf.WriteStringLn(":")

			if form.Node.EncType == "multipart/form-data" {
				buf.WriteStringLn(`if !form.IsMultipart(r) {`)
				buf.WriteStringLn(`	return nil, mux.NewHttpError(http.StatusBadRequest, "Content-Type is not multipart/form-data")`)
				buf.WriteStringLn("}")
			} else {
				buf.WriteStringLn(`if form.IsMultipart(r) {`)
				buf.WriteStringLn(`	return nil, mux.NewHttpError(http.StatusBadRequest, "Content-Type is not application/x-www-form-urlencoded")`)
				buf.WriteStringLn("}")
			}

			for _, element := range form.Elements {
				buf.WriteString("dataCtx." + formField + "." + getExportedName(element.Name) + ".Process(r, ")
				buf.WriteQuotedString(element.Name, ", ")

				if form.Node.EncType == node.FormEncTypeMultipart {
					buf.WriteString("true, ")
				} else {
					buf.WriteString("false, ")
				}

				if element.IsRequired {
					buf.WriteString("true")
				} else {
					buf.WriteString("false")
				}
				buf.WriteStringLn(")")
			}

			buf.WriteStringLn("if err := rt.dp." + getFormProcessMethod(form.Name) + "(ctx, r, w, &dataCtx." +
				formField + "); err != nil {")
			buf.WriteStringLn("	return nil, err")
			buf.WriteStringLn("	}")

			buf.WriteStringLn("dataCtx." + formField + ".BaseFormValues.MarkValidated()")
		}
		buf.WriteStringLn("	}")
		buf.WriteStringLn("}")

		buf.WriteString("if err := form.SetCSRFToken(r, w")
		for _, form := range r.Template().GetForms() {
			buf.WriteString(",")
			buf.WriteString("&dataCtx." + getFormCtxField(form.Name) + ".BaseFormValues")
		}
		buf.WriteStringLn("); err != nil {")
		buf.WriteStringLn("	return nil, err")
		buf.WriteStringLn("}")
	}

	buf.WriteStringLn("if err := rt.dp.Data(ctx, r, w, &dataCtx.RouteData); err != nil {")
	buf.WriteStringLn("return nil, err")
	buf.WriteStringLn("}")
	buf.WriteString("\n")
	buf.WriteStringLn("return dataCtx, nil")
	buf.WriteStringLn("}")
	buf.WriteString("\n")

	// func (rt *ssrRoute) GetDefaultRoute
	buf.WriteString("func (rt *ssrRoute) GetDefaultRoute(ctx context.Context, r *mux.Request) (string, error) { return ")
	if nodeContent := r.Template().GetContentNode(); nodeContent != nil {
		if nodeContent.Default == "" {
			buf.WriteString("rt.dp.DefaultRoute(ctx, r)")
		} else {
			buf.WriteQuotedString(nodeContent.Default)
			buf.WriteString(", nil")
		}
	} else {
		buf.WriteString(`"", nil`)
	}

	buf.WriteStringLn("}")

	// type dataContext
	buf.WriteStringLn("type dataContext struct {")
	buf.WriteStringLn("mux.RouteDataContext")
	buf.WriteStringLn("RouteData RouteData")
	for _, form := range r.Template().GetForms() {
		buf.WriteStringLn(getFormCtxField(form.Name) + " " + getFormValuesTypeName(form.Name))
	}
	buf.WriteStringLn("}")

	// func (c *dataContext) Write
	buf.WriteStringLn("func (c *dataContext) Write(w io.Writer) error {")
	for _, variable := range routeVariables {
		buf.WriteStringLn(variable.FilePos())
		buf.WriteString(variable.Name)
		buf.WriteString(":=c.RouteData.")
		buf.WriteStringLn(getExportedName(variable.Name))
	}
	r.Template().WriteGoCode(buf)
	buf.WriteStringLn("return nil")
	buf.WriteStringLn("}")

	buf.WriteVars()

	formattedCode, err := buf.Formatted()
	if err != nil {
		return fmt.Errorf("could not format generated code: %w\n%s", err, buf.String())
	}

	if err := os.WriteFile(filepath.Join(g.webDir, "pages", rPath, "ssrroute_gen.go"), formattedCode, 0755); err != nil {
		return fmt.Errorf("could not write route.go: %w", err)
	}

	return nil
}

func (g *Generator) genRouteDP(rPath string, r *route.Route) error {
	filename := filepath.Join(g.webDir, "pages", rPath, "dataprovider.go")
	if fileExists(filename) {
		// Already exists, skip
		return nil
	}

	buf := gobuf.New()

	buf.WriteString("package ")
	buf.WriteStringLn(path.Base(path.Join("pages", rPath)))

	buf.WriteStringLn("import (")
	buf.WriteQuotedString("context", "\n\n")
	buf.WriteQuotedString("github.com/sergei-svistunov/go-ssr/pkg/mux", "\n")
	if g.depsPackage != "" {
		buf.WriteQuotedString(g.depsPackage, "\n")
	}
	buf.WriteStringLn(")")

	buf.WriteStringLn("var _ RouteDataProvider = &DP{}")

	buf.WriteStringLn("type DP struct{}")

	if g.depsPackage != "" {
		buf.WriteStringLn("func NewDP(d *" + g.depsPackageName() + "." + g.depsType + ") *DP {")
	} else {
		buf.WriteStringLn("func NewDP() *DP {")
	}
	buf.WriteStringLn("	return &DP{}")
	buf.WriteStringLn("}\n")

	if r.Template().GetContentNode() != nil && r.Template().GetContentNode().Default == "" {
		buf.WriteStringLn("func (p *DP) DefaultRoute(ctx context.Context, r *mux.Request) (string, error) {")
		buf.WriteStringLn("	return \"\", nil")
		buf.WriteStringLn("}\n")
	}

	buf.WriteStringLn("func (p *DP) Data(ctx context.Context, r *mux.Request, w mux.ResponseWriter, data *RouteData) error {")
	buf.WriteStringLn("	return  nil")
	buf.WriteStringLn("}")

	formattedCode, err := buf.Formatted()
	if err != nil {
		return fmt.Errorf("could not format generated code: %w\n%s", err, buf.String())
	}

	if err := os.WriteFile(filename, formattedCode, 0755); err != nil {
		return fmt.Errorf("could not write dataprovider.go: %w", err)
	}

	return nil
}

func pathToVariable(path string) string {
	if path == "" || path == "/" {
		return "_"
	}

	var res string

	for _, segment := range strings.Split(path, "/") {
		if segment == "" {
			continue
		}

		res += strings.ToUpper(string(segment[0])) + segment[1:]
	}

	return res
}

func getFormValuesTypeName(formName string) string {
	return "Form" + getExportedName(formName) + "Values"
}

func getFormInitMethod(formName string) string {
	return "Init" + getExportedName(formName)
}

func getFormProcessMethod(formName string) string {
	return "Process" + getExportedName(formName)
}

func getFormCtxField(formName string) string {
	return "Form" + getExportedName(formName) + "Data"
}

func getRoutePackageAlias(rPath string) string {
	return "route" + pathToVariable(rPath)
}

func getExportedName(name string) string {
	if name == "" {
		return ""
	}
	return strings.ToUpper(string(name[0])) + name[1:]
}

func fileExists(filename string) bool {
	_, err := os.Stat(filename)
	return err == nil
}

func fileTime(filename string) (time.Time, error) {
	stat, err := os.Stat(filename)
	if err != nil {
		return time.Time{}, err
	}
	return stat.ModTime(), nil
}

func isDirEmpty(p string) bool {
	entries, err := os.ReadDir(p)
	if err != nil {
		return true
	}
	return len(entries) == 0
}
